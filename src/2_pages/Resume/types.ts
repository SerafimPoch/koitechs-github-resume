import { z } from "zod";

const ownerSchema = z.object({
  login: z.string(),
  id: z.number(),
  nodeId: z.string(),
  avatarUrl: z.string(),
  gravatarId: z.string().optional(),
  url: z.string(),
  htmlUrl: z.string(),
  followersUrl: z.string(),
  followingUrl: z.string(),
  gistsUrl: z.string(),
  starredUrl: z.string(),
  subscriptionsUrl: z.string(),
  organizationsUrl: z.string(),
  reposUrl: z.string(),
  eventsUrl: z.string(),
  receivedEventsUrl: z.string(),
  type: z.string(),
  siteAdmin: z.boolean(),
});

export const repoSchema = z.object({
  id: z.number(),
  nodeId: z.string(),
  name: z.string(),
  fullName: z.string(),
  private: z.boolean(),
  owner: ownerSchema,
  htmlUrl: z.string(),
  description: z.string().nullable(),
  fork: z.boolean(),
  url: z.string(),
  forksUrl: z.string(),
  keysUrl: z.string(),
  collaboratorsUrl: z.string(),
  teamsUrl: z.string(),
  hooksUrl: z.string(),
  issueEventsUrl: z.string(),
  eventsUrl: z.string(),
  assigneesUrl: z.string(),
  branchesUrl: z.string(),
  tagsUrl: z.string(),
  blobsUrl: z.string(),
  gitTagsUrl: z.string(),
  gitRefsUrl: z.string(),
  treesUrl: z.string(),
  statusesUrl: z.string(),
  languagesUrl: z.string(),
  stargazersUrl: z.string(),
  contributorsUrl: z.string(),
  subscribersUrl: z.string(),
  subscriptionUrl: z.string(),
  commitsUrl: z.string(),
  gitCommitsUrl: z.string(),
  commentsUrl: z.string(),
  issueCommentUrl: z.string(),
  contentsUrl: z.string(),
  compareUrl: z.string(),
  mergesUrl: z.string(),
  archiveUrl: z.string(),
  downloadsUrl: z.string(),
  issuesUrl: z.string(),
  pullsUrl: z.string(),
  milestonesUrl: z.string(),
  notificationsUrl: z.string(),
  labelsUrl: z.string(),
  releasesUrl: z.string(),
  deploymentsUrl: z.string(),
  createdAt: z.string(),
  updatedAt: z.string(),
  pushedAt: z.string(),
  gitUrl: z.string(),
  sshUrl: z.string(),
  cloneUrl: z.string(),
  svnUrl: z.string(),
  homepage: z.string().nullable(),
  size: z.number(),
  stargazersCount: z.number(),
  watchersCount: z.number(),
  language: z.string().nullable(),
  hasIssues: z.boolean(),
  hasProjects: z.boolean(),
  hasDownloads: z.boolean(),
  hasWiki: z.boolean(),
  hasPages: z.boolean(),
  forksCount: z.number(),
  mirrorUrl: z.string().nullable(),
  archived: z.boolean(),
  disabled: z.boolean(),
  openIssuesCount: z.number(),
  license: z
    .object({
      key: z.string(),
      name: z.string(),
      spdxId: z.string(),
      url: z.string().nullable(),
      nodeId: z.string(),
    })
    .nullable(),
  forks: z.number(),
  openIssues: z.number(),
  watchers: z.number(),
  defaultBranch: z.string(),
});

export type GitHubRepo = z.infer<typeof repoSchema>;
